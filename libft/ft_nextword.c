void	ft_nextword(char **tab, char c)
{
	char	*str;
	int		i;
	int		j;

	j = 0;
	str = *tab;
	i = 0;
	while (str[j])
	{
		if (str[j] == c)
		{
			i = 1;
		}
		else if (str[j] != c && i == 1)
		{
			return ;
		}
		*tab = *tab + 1;
		j++;
	}
}
