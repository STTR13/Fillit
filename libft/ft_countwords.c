int		ft_countwords(const char *str, char c)
{
	int	j;
	int	k;
	int	i;

	j = 0;
	k = 0;
	i = 0;
	while (str[i])
	{
		if (str[i] == c)
		{
			i++;
			j = 0;
		}
		else
		{
			if (j == 0)
			{
				j = 1;
				k++;
			}
			i++;
		}
	}
	return (k);
}
